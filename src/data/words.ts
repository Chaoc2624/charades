export const ALL_CATEGORIES: Category[] = [
  'nouns', 'verbs', 'adjectives', 'animals', 'food', 'occupations',
  'movies', 'tv-shows', 'celebrities', 'sports', 'places',
]
export const ALL_AUDIENCES: Audience[] = ['all-ages', 'kids', 'adults']

// ─── Translation lookup ─── //
import { wordTranslations } from './word-translations'

export function getWords(
  lang: string,
  category?: Category,
  audience?: Audience,
): Word[] {
  const translations = lang !== 'en' ? wordTranslations[lang] : undefined

  return masterWords
    .filter((w) => {
      if (category && w.category !== category) return false
      if (audience && audience !== 'all-ages' && w.audience !== audience && w.audience !== 'all-ages') return false
      return true
    })
    .map((w) => {
      if (!translations) return w
      const translated = translations[w.text]
      return translated ? { ...w, text: translated } : w
    })
}

export function getRandomWord(
  lang: string,
  category?: Category,
  audience?: Audience,
  exclude?: string,
): Word {
  const available = getWords(lang, category, audience).filter((w) => w.text !== exclude)
  if (available.length === 0) {
    return { text: '?', category: 'nouns', audience: 'all-ages' }
  }
  return available[Math.floor(Math.random() * available.length)]
}

/** Get multiple unique random words at once */
export function getRandomWords(
  lang: string,
  count: number,
  category?: Category,
  audience?: Audience,
): Word[] {
  const available = [...getWords(lang, category, audience)]
  const result: Word[] = []
  const n = Math.min(count, available.length)
  for (let i = 0; i < n; i++) {
    const idx = Math.floor(Math.random() * available.length)
    result.push(available[idx])
    available.splice(idx, 1)
  }
  return result
}

export function isValidCategory(cat: string): cat is Category {
  return ALL_CATEGORIES.includes(cat as Category)
}

export function isValidAudience(aud: string): aud is Audience {
  return ALL_AUDIENCES.includes(aud as Audience)
}
